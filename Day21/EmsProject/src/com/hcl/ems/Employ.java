package com.hcl.ems;

import java.util.Date;

public class Employ {
	private int empId;
	private String empName;
	private String empEmail;
	private int empMobNo;
	private String empDeptName;
	private Date empDateJoined;
	private int empMgrId;
	private int empLeaveBal;
	
	public int getEmpId() {
		return empId;
	}
	public void setEmpId(int empId) {
		this.empId = empId;
	}
	public String getEmpName() {
		return empName;
	}
	public void setEmpName(String empName) {
		this.empName = empName;
	}
	public String getEmpEmail() {
		return empEmail;
	}
	public void setEmpEmail(String empEmail) {
		this.empEmail = empEmail;
	}
	public int getEmpMobNo() {
		return empMobNo;
	}
	public void setEmpMobNo(int empMobNo) {
		this.empMobNo = empMobNo;
	}
	public String getEmpDeptName() {
		return empDeptName;
	}
	public void setEmpDeptName(String empDeptName) {
		this.empDeptName = empDeptName;
	}
	public Date getEmpDateJoined() {
		return empDateJoined;
	}
	public void setEmpDateJoined(Date empDateJoined) {
		this.empDateJoined = empDateJoined;
	}
	public int getEmpMgrId() {
		return empMgrId;
	}
	public void setEmpMgrId(int empMgrId) {
		this.empMgrId = empMgrId;
	}
	public int getEmpLeaveBal() {
		return empLeaveBal;
	}
	public void setEmpLeaveBal(int empLeaveBal) {
		this.empLeaveBal = empLeaveBal;
	}

}
